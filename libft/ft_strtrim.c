#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;
	char	*k;

	k = 0;
	if (!s1 || !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	k = (char *)malloc(sizeof(char ) * (end - start +1));
	if (!k)
		return (NULL);
	ft_strlcpy(k, &s1[start], end - start + 1);
	return (k);
}
